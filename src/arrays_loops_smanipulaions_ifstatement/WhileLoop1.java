package arrays_loops_smanipulaions_ifstatement;

public class WhileLoop1 {

	public static void main(String[] args) {
		
		//  While loop kullanarak 3'den 13'e kadar tüm tek tam sayýlarý ekrana yazdýrýnýz.
		
		
		int sayi=2;
		
		while (sayi<=13) {
		
			if (sayi%2==1) {
				System.out.println("Tek sayýlar : " + sayi);
			}
			
			sayi++;
		}
		
		
		
		
		
		
		

	}

}
